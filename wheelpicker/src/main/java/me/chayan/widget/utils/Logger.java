package me.chayan.widget.utils;

import android.util.Log;

/**
 * Created by Chayan on 2018/8/24.
 */
public final class Logger {

    private static int sLevel = Log.VERBOSE;

    private Logger() {
        throw new RuntimeException();
    }

    public static void setLevel(int level) {
        sLevel = level;
    }

    /**
     * Send a Log.VERBOSE log message.
     * @param tag Used to identify the source of a log message.  It usually identifies
     *        the class or activity where the log call occurs.
     * @param msg The message you would like logged.
     */
    public static int v(String tag, String msg) {
        if (sLevel > Log.VERBOSE) return 0;
        return Log.v(tag, msg);
    }

    /**
     * Send a Log.DEBUG log message.
     * @param tag Used to identify the source of a log message.  It usually identifies
     *        the class or activity where the log call occurs.
     * @param msg The message you would like logged.
     */
    public static int d(String tag, String msg) {
        if (sLevel > Log.DEBUG) return 0;
        return Log.d(tag, msg);
    }

    /**
     * Send an Log.INFO log message.
     * @param tag Used to identify the source of a log message.  It usually identifies
     *        the class or activity where the log call occurs.
     * @param msg The message you would like logged.
     */
    public static int i(String tag, String msg) {
        if (sLevel > Log.INFO) return 0;
        return Log.i(tag, msg);
    }

    /**
     * Send a Log.WARN log message.
     * @param tag Used to identify the source of a log message.  It usually identifies
     *        the class or activity where the log call occurs.
     * @param msg The message you would like logged.
     */
    public static int w(String tag, String msg) {
        if (sLevel > Log.WARN) return 0;
        return Log.w(tag, msg);
    }

    /**
     * Send an Log.ERROR log message.
     * @param tag Used to identify the source of a log message.  It usually identifies
     *        the class or activity where the log call occurs.
     * @param msg The message you would like logged.
     */
    public static int e(String tag, String msg) {
        if (sLevel > Log.ERROR) return 0;
        return Log.e(tag, msg);
    }
}
